# 3. Determinar la edad de una persona conociendo el año actual y el año de nacimiento.
#ENTRADA DE DATOS
# Declaracion o creación de variables
año_actual = float(input("Ingrese año actual: "))
año_de_nacimiento = float(input("Ingrese año de nacimiento: "))




# PROCESOS
edad = año_actual - año_de_nacimiento




# SALIDA DE DATOS
print("Edad de persona =", edad)